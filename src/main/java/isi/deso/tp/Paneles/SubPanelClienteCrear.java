    private void CrearClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearClienteActionPerformed
        
        String direccion = getDireccionCliente();
        String email = getEmail();
        double latitud = getLatitud();  
        double longitud = getLongitud();
        String CUIT = getCuit();
        
        if (direccion.isEmpty() || email.isEmpty() || CUIT.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor complete todos los campos.");
            return;
        }

        if (latitud <= 0.0 || longitud <= 0.0 ) {
            JOptionPane.showMessageDialog(null, "Latitud y Longitud deben ser vÃ¡lidos.");
            return;
        }
        
        if(!validation.ValidarSoloNumeros(CUIT)){
            JOptionPane.showMessageDialog(null, "El CUIT debe contener solo digitos");
            return;
        }
        if(validation.ValidarCliente(CUIT)){
            JOptionPane.showMessageDialog(null, "Ya existe un cliente con el cuit: "+ CUIT);
            return;
        }
        
        clienteController.crearCliente(direccion, email, latitud, longitud, CUIT);
        JOptionPane.showMessageDialog(null, "El cliente se ha creado con exito");
    }//GEN-LAST:event_CrearClienteActionPerformed

